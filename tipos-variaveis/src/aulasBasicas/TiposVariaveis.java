package aulasBasicas;
public class TiposVariaveis {
    public static void main(String[] args) throws Exception {
        byte tipoByte = 124;
        short tipoShort= 3000;
        int tipoInt = 2000000000;
        long tipoLong = 9000000000000000000L;
        float tipoFloat = 9.2F;
        double tipoDouble = 2.5;

        String tipoString = "Nathan Tanzi";
        
        final double PI = 3.14;

        System.out.println(tipoString +"\n"+ tipoByte +"\n"+ tipoShort +"\n"+ tipoInt +"\n"+ tipoLong +"\n"+
         tipoFloat +"\n"+ tipoDouble +"\n"+ PI);

        
    }
}
